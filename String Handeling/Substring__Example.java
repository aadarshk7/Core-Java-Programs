public class Substring__Example {
	public static void main(String[] args) {
		String s = "            aadarshkunwar";
		String s2 = "kunwaraadarsh";

		System.out.println("hello" + 2 + 0 + (2+3));
		System.out.println(s.substring(0, 7));
		System.out.println(s2.substring(6, 13));
		System.out.println(s.replace('a', 'p'));
		System.out.println(s.trim());
	}
}
